package com.example.schoolproject.Modal;

public class StatisticsModal {
    private String day;
    private int cnt;

    public StatisticsModal(int cnt) {
        this.day = day;
        this.cnt = cnt;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public int getCnt() {
        return cnt;
    }

    public void setCnt(int cnt) {
        this.cnt = cnt;
    }
}
